function calcHarmonic(arr) {
	var sum = 0
	arr.forEach(function (val) {
		// console.log(typeof val, val)
		sum += 1/(1-Math.min(0.99, val))
	})
	return 1 - (arr.length/sum)
}

function avgTwoLargest(arr) {
	var one=0, two=0
	arr.forEach(function (d) {
		if (one == 0) {
			one = d 
		} else if (two == 0) {
			two = d
		} else if (d > two) {
			if (d > one) {
				two = one
				one = d
			} else {
				two = d
			}
		}

	})
	return (one+two)/2
}

