/**
	Adds a toss to the sequence
	@param {String} Must be "heads" to represent a head.
					Any other value will be interpreted as tails
					Case sensitive
**/
function addToSequence(coin) {
	charCount++
	if (coin == "heads") {
		seq += "1"
		displayCharacter(0)
	} else {
		seq += "0"
		displayCharacter(1)
	}
	if (charCount == 200) {
		socket.emit("addString", {rString : seq})
	}
	calcHumanProb(seq)
}

